public class World {
    public static void main(String[] args){
        System.out.println("Indra");

        //Non-Primitive Variables
        //Declaration

        //value declaration: variableType variableName; (Here we use Camel Case to separate the 'variable' and 'Type')
        String str;

        //declaration of array: arrayType[] arrayName;
        int[] array1;
        String[] array2;

        //Assignment

        //value assignment: variableName = variableValue;
        str = "Indra";

        //assignment of array: arrayName = new arrayType[value];
        array1 = new int[5];
        array2 = new String[5];

        array1[0] = 2;
        array1[1] = 3;
        array1[2] = 4;
        array1[3] = 5;
        array1[4] = 6;

        //for String the value should always be in double quotes (" ")
        array2[0] = "Indra";
        array2[1] = "Mohammad";
        array2[2] = "Naser";
        array2[3] = "Jaber";
        array2[4] = "Atik";






    }
}
